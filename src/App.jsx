import React, { useState } from 'react';
import './index.css';

const App = () => {
  const [input, setInput] = useState('');
  const [gherkin, setGherkin] = useState(true);
  const [testCases, setTestCases] = useState('');
  const [loading, setLoading] = useState(false);

  const generateTestCases = async () => {
    if (!input.trim()) return;
    setLoading(true);
    setTestCases('');
    try {
      const response = await fetch('https://test-case-backend.onrender.com/generate-test-cases', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ input, gherkin })
      });
      const data = await response.json();
      setTestCases(data.testCases || 'No test cases generated.');
    } catch (error) {
      setTestCases('âŒ Error generating test cases. Please try again later.');
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="min-h-screen dark:bg-gray-900 bg-white text-gray-800 dark:text-gray-100 p-4 flex flex-col items-center">
      <div className="w-full max-w-4xl p-6 rounded-xl border border-dashed border-orange-400 shadow-lg dark:shadow-white">
        <div className="flex items-center mb-6">
          <img src="/bk-icon.png" alt="BK" className="h-8 w-8 mr-2" />
          <h1 className="text-3xl font-bold">Test Case Generator</h1>
        </div>
        <label className="flex items-center mb-2">
          <input type="checkbox" checked={gherkin} onChange={() => setGherkin(!gherkin)} className="mr-2" />
          Gherkin Format
        </label>
        <textarea
          className="w-full h-40 p-3 border border-gray-300 dark:border-gray-700 rounded mb-4 text-black dark:text-white resize-y"
          placeholder="Paste acceptance criteria or feature description..."
          value={input}
          onChange={(e) => setInput(e.target.value)}
        />
        <button
          onClick={generateTestCases}
          disabled={loading}
          className="bg-blue-600 hover:bg-blue-700 text-white px-5 py-2 rounded mb-4 disabled:opacity-50"
        >
          {gherkin ? 'Generate Gherkin Test Cases' : 'Generate Test Cases'}
        </button>
        <div className="mt-4">
          <h2 className="text-xl font-semibold mb-2">Generated Test Cases:</h2>
          <p className="text-sm mb-2 text-yellow-400">
            âš ï¸ These test cases are generated by AI and may still contain errors. Please review with your human brain ğŸ§ .
          </p>
          <pre className="bg-gray-100 dark:bg-gray-800 p-4 rounded max-h-96 overflow-auto text-sm">
            {testCases || 'No test cases generated yet.'}
          </pre>
        </div>
      </div>
    </div>
  );
};

export default App;