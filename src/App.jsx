import React, { useState } from "react";
import axios from "axios";

function App() {
  const [input, setInput] = useState("");
  const [output, setOutput] = useState("");
  const [loading, setLoading] = useState(false);
  const [gherkin, setGherkin] = useState(true);

  const handleGenerate = async () => {
    if (!input.trim()) return;
    setLoading(true);
    try {
      const response = await axios.post(
        "https://test-case-backend.onrender.com/generate-test-cases",
        {
          input,
          gherkin,
        }
      );
      setOutput(response.data.output);
    } catch (error) {
      setOutput("âŒ Failed to generate test cases. Please try again.");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="min-h-screen bg-gray-900 text-white flex flex-col items-center justify-start p-4 space-y-6">
      <h1 className="text-3xl font-bold mt-4">ğŸ§ª Test Case Generator</h1>

      <div className="w-full max-w-3xl flex flex-col items-center">
        <label className="flex items-center text-sm mb-2">
          <input
            type="checkbox"
            checked={gherkin}
            onChange={() => setGherkin(!gherkin)}
            className="mr-2"
          />
          Gherkin Format
        </label>

        <textarea
          className="w-full text-black p-3 rounded-lg border border-gray-300 text-base min-h-[12rem] resize-none"
          placeholder="Paste acceptance criteria or feature description..."
          value={input}
          onChange={(e) => setInput(e.target.value)}
        />

        <div className="flex gap-4 mt-4">
          <button
            onClick={handleGenerate}
            className="bg-blue-600 px-6 py-2 rounded text-white font-semibold disabled:opacity-50"
            disabled={loading}
          >
            {loading ? "Generating..." : "Generate Test Cases"}
          </button>
        </div>
      </div>

      {output && (
        <div className="w-full max-w-4xl mt-8">
          <h2 className="text-xl font-semibold mb-2">âœ… Generated Test Cases:</h2>
          <div className="bg-gray-800 text-green-300 p-4 rounded overflow-auto whitespace-pre-wrap max-h-[500px]">
            <pre>{output}</pre>
          </div>
          <p className="mt-2 text-yellow-400 text-sm">
            âš ï¸ These test cases are generated by AI and may still contain errors. Please review with your human brain ğŸ§ .
          </p>
        </div>
      )}
    </div>
  );
}

export default App;