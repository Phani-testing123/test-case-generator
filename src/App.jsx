import React, { useState } from "react";
import axios from "axios";

const App = () => {
  const [input, setInput] = useState("");
  const [gherkinOnly, setGherkinOnly] = useState(true);
  const [testCases, setTestCases] = useState("");
  const [loading, setLoading] = useState(false);

  const handleSubmit = async () => {
    if (!input.trim()) return;
    setLoading(true);
    try {
      const response = await axios.post(
        "https://test-case-backend.onrender.com/generate-test-cases",
        {
          input,
          gherkinOnly,
        }
      );
      setTestCases(response.data.testCases);
    } catch (error) {
      console.error("Error generating test cases:", error);
      setTestCases("❌ Failed to generate test cases. Please try again.");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="min-h-screen bg-gradient-to-br from-gray-100 to-blue-100 dark:from-gray-800 dark:to-gray-900 text-gray-900 dark:text-gray-100 p-6">
      <div className="max-w-5xl mx-auto py-10 px-6 bg-white dark:bg-gray-900 rounded-xl shadow-lg">
        <h1 className="text-3xl md:text-4xl font-bold mb-6 flex items-center">
          <span role="img" aria-label="test">🧪</span> Test Case Generator
        </h1>

        <div className="flex items-center mb-4">
          <input
            id="gherkinOnly"
            type="checkbox"
            checked={gherkinOnly}
            onChange={() => setGherkinOnly(!gherkinOnly)}
            className="mr-2"
          />
          <label htmlFor="gherkinOnly" className="text-base md:text-lg font-medium">
            Gherkin Format
          </label>
        </div>

        <textarea
          rows="10"
          value={input}
          onChange={(e) => setInput(e.target.value)}
          placeholder="Paste acceptance criteria or feature description..."
          className="w-full p-4 text-base rounded-md border border-gray-300 dark:border-gray-700 bg-gray-50 dark:bg-gray-800 resize-none focus:outline-none focus:ring-2 focus:ring-blue-400"
        ></textarea>

        <button
          onClick={handleSubmit}
          disabled={loading}
          className="mt-4 px-6 py-2 text-white font-semibold bg-blue-600 rounded hover:bg-blue-700 disabled:opacity-60"
        >
          {loading ? "Generating..." : `Generate ${gherkinOnly ? "Gherkin " : ""}Test Cases`}
        </button>

        <div className="mt-8">
          <h2 className="text-xl font-semibold mb-2">Generated Test Cases:</h2>
          <p className="text-sm text-yellow-600 dark:text-yellow-300 mb-4">
            ⚠️ These test cases are generated by AI and may still contain errors. Please review with your human brain 🧠.
          </p>
          <pre className="whitespace-pre-wrap bg-gray-100 dark:bg-gray-800 p-4 rounded-md overflow-auto max-h-[500px] text-sm md:text-base">
            {testCases || "No test cases generated yet."}
          </pre>
        </div>
      </div>
    </div>
  );
};

export default App;